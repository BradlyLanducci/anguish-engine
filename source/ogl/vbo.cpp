#include "vbo.h"
#include <ogl/gl_error.h>

#include <glad/glad.h>

//------------------------------------------------------------------//

VBO::VBO()
{
	glGenBuffers(1, &m_id);
	checkGLError();
}

//------------------------------------------------------------------//

uint32_t& VBO::get()
{
	return m_id;
}

//------------------------------------------------------------------//

uint32_t VBO::get() const
{
	return GlObject::get();
}

