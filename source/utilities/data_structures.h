#pragma once

//------------------------------------------------------------------//

#include <cstdint>

//------------------------------------------------------------------//

struct Vector2
{
	Vector2(float _x, float _y)
			: x(_x)
			, y(_y)
	{
	}

	Vector2()
	{
	}

	float x{0.f};
	float y{0.f};
};

//------------------------------------------------------------------//

struct Vector2i
{
	Vector2i(uint32_t _x, uint32_t _y)
			: x(_x)
			, y(_y)
	{
	}

	Vector2i()
	{
	}

	uint32_t x{0};
	uint32_t y{0};
};

//------------------------------------------------------------------//
